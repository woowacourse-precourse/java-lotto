package lotto;

import org.assertj.core.api.Assert;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import java.util.List;

import static org.assertj.core.api.Assertions.assertThat;
import static org.assertj.core.api.Assertions.assertThatThrownBy;
import static org.junit.jupiter.api.Assertions.fail;
import static org.junit.platform.commons.function.Try.success;

class LottoTest {
    @DisplayName("로또 번호의 개수가 6개가 넘어가면 예외가 발생한다.")
    @Test
    void createLottoByOverSize() {
        assertThatThrownBy(() -> new Lotto(List.of(1, 2, 3, 4, 5, 6, 7)))
                .isInstanceOf(IllegalArgumentException.class);
    }

    @DisplayName("로또 번호에 중복된 숫자가 있으면 예외가 발생한다.")
    @Test
    void createLottoByDuplicatedNumber() {
        // TODO: 이 테스트가 통과할 수 있게 구현 코드 작성
        assertThatThrownBy(() -> new Lotto(List.of(1, 2, 3, 4, 5, 5)))
                .isInstanceOf(IllegalArgumentException.class);
    }

    // 아래에 추가 테스트 작성 가능

    @DisplayName("입력받은 금액이 1000으로 나누어 떨어지지 않으면 예외가 발생해야한다.")
    @Test
    void createMoneyByNotDivideBy1000(){
        try{
            Application.checkCanDivideBy1000(1005);
            fail("IllegalArgumentException이 발생해야 한다.");
        }catch(IllegalArgumentException IllegalArgumentException){
            success("IllegalArgumentException이 발생했다.");
        }
    }

    @DisplayName("로또 번호와 당첨번호의 일치 개수를 반환한다.")
    @Test
    void countNumbersWithCorrectNumbers(){
        Lotto lotto = new Lotto(List.of(1, 2, 3, 4, 5, 6));
        int[] correctNum = {1, 1, 1, 4, 5, 6};
        assertThat(lotto.getCountCorrectNumbers(correctNum)).isEqualTo(4);
    }

    @DisplayName("로또 번호에 보너스 번호가 있는지 판단한다.")
    @Test
    void isThereInBonusNumber(){
        Lotto lotto = new Lotto(List.of(1, 2, 3, 4, 5, 6));
        int bonusNum = 5;
        assertThat(lotto.isSameWithBonumNum(bonusNum)).isEqualTo(true);
        bonusNum = 7;
        assertThat(lotto.isSameWithBonumNum(bonusNum)).isEqualTo(false);
    }
}
