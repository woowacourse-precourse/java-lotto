package lotto;

import org.junit.jupiter.api.Test;

import static org.assertj.core.api.Assertions.assertThat;

class RankTest {
    @Test
    public void rankTest() {
        assertThat(Rank.valueOf("FIRST").getRank()).isEqualTo(1);
        assertThat(Rank.valueOf("SECOND").getRank()).isEqualTo(2);
        assertThat(Rank.valueOf("THIRD").getRank()).isEqualTo(3);
        assertThat(Rank.valueOf("FORTH").getRank()).isEqualTo(4);
        assertThat(Rank.valueOf("FIFTH").getRank()).isEqualTo(5);
    }

}